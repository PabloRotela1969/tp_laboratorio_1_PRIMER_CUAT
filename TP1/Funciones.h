#include <stdio.h>
#include <stdlib.h>

int suma(int primero,int segundo)
{
    return primero + segundo;
}

int resta(int primero,int segundo)
{
    return primero - segundo;
}

int multiplicacion(int primero, int segundo)
{
    return primero * segundo;
}

float division(int primero, int segundo)
{
   return (float)primero / segundo;

}
